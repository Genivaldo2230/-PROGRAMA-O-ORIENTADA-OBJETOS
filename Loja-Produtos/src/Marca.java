public class Marca extends Produtos {
    private String nome;
    private String marca;

    public Marca(String nome, String marca) {
        this.nome = nome;
        this.marca = marca;
    }


    public Marca() {
        this.nome = nome;
        this.marca = marca;
    }

    public String getNome() {

        this.nome = nome;
        return nome;
    }

    public String getMarca() {
        this.marca = marca;
        return marca;
    }











}
