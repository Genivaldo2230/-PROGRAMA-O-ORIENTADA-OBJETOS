public class Marca {


    private String nomeMarca;
    private String marca;


    public Marca(String nomeMarca, String marca) {
        this.nomeMarca = nomeMarca;
        this.marca = marca;
    }

    public Marca() {
        this.nomeMarca = nomeMarca;
        this.marca = marca;
    }

    public String getNomeMarca() {
        return nomeMarca;
    }

    public String getMarca() {
        return marca;
    }
}
